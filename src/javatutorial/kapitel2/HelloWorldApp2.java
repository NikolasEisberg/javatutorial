package javatutorial.kapitel2;

public class HelloWorldApp2 {
    public static void main(String[] args) {
        String helloWorld = "Hello World";
        System.out.println(helloWorld);
        
        boolean wahrheit = true;
        System.out.println(wahrheit);
                
        byte byteWert = 10;
        System.out.println(byteWert);
        
        char zeichen = 'A';
        System.out.println(zeichen);
        
        double kommaZahl = 3.2;
        System.out.println(kommaZahl);
        
        float weitereKommaZahl = (float) 6.4;
        System.out.println(weitereKommaZahl);
        
        int zahl = 2017;
        System.out.println(zahl);
        
        long andereZahl = 1989;
        System.out.println(andereZahl);
        
        short kleineZahl = 27;
        System.out.println(kleineZahl);
    }
}
